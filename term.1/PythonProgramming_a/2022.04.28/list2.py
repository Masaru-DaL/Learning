
eto_list = ['ね', 'うし', 'とら', 'う', 'たつ', 'み', 'うま', 'ひつじ', 'さる', 'とり', 'いぬ', 'い']
print(eto_list)

# 配列の3~5を取り出す
print(eto_list[3:5])
# 配列の5~後ろ全部
print(eto_list[5:])
# 配列全部
print(eto_list[:])
# 後ろから逆に指定しまうとエラーになる
print(eto_list[-5:-3])
# 後ろから5番目から後ろへ
print(eto_list[-5:])
# 前から順に、後ろから5番目まで
print(eto_list[:-5])
